# Definimos un arreglo 
meses = ["Enero","Febrero", "Marzo", "Abril", "Mayo", "Junio","Julio","Agosto","Septiembre","Octubre","Noviembre","Diciembre"]

# Iteramos el contenido de la lista 
for mes in meses:
    print(f' Estamos listando el mes de {mes}')

# Iteramos el contenido de la lista por rango
for mes in range(0,11):
        print( meses[mes])